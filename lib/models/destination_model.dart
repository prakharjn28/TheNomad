class Destination {
  final int id;
  final String name;
  final String description;
  final String shortLocation;
  final String longLocation;
  final double rating;
  final String imageUrl;
  final double latitude;
  final double longitude;

  Destination(
      this.id,
      this.name,
      this.description,
      this.shortLocation,
      this.longLocation,
      this.rating,
      this.imageUrl,
      this.latitude,
      this.longitude);
}
